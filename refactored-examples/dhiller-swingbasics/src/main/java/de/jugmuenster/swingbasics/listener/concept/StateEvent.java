package de.jugmuenster.swingbasics.listener.concept;

/**
 * The event data container.
 * <p>
 * This is a simple demo implementation. Events can be used to transport the
 * source, the date and time and other useful data that the clients need.
 */
class StateEvent {

    /**
     * The state that was current when the event was published.
     */
    final String state;

    StateEvent(String state) {
	this.state = state;
    }

    @Override
    public String toString() {
	return "StateEvent [state=" + state + "]";
    }

}